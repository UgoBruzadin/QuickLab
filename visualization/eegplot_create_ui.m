  u(57) = uicontrol('Parent', panels.infoTab, 'Units', 'normalized', ...
    'Position', [margin y tw hval], 'Style', 'popupmenu', ...
    'Tag', 'FolderList', 'string', '', 'callback', loadfilecommand);
