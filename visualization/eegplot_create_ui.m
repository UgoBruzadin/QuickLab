function [g, u, m, modecolor, cmodecolor] = eegplot_create_ui(figh, g, data, EEG, ax0, ax1, colors, panels)
% eegplot_create_ui() - Create all UI controls and menus for eegplot_adv
%
% Usage:
%   [g, u, m, modecolor, cmodecolor] = eegplot_create_ui(figh, g, data, EEG, ax0, ax1, colors, panels)
%
% Inputs:
%   figh   - figure handle
%   g      - eegplot_adv userdata struct
%   data   - EEG data matrix
%   EEG    - EEG struct
%   ax0    - background axes handle
%   ax1    - EEG data axes handle
%   colors - struct with color/style defaults:
