  %% ========================================================================
  %  MARKS TAB - stacked vertically
  %  ========================================================================
  y = 0.94;

  % Heatmap/topoplot title
  u(24) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin y tw h], 'Style', 'text', 'FontSize', 8, ...
    'Tag', 'headmap', 'BackgroundColor', DEFAULT_FIG_COLOR, ...
    'string', 'Current Data Marks', 'TooltipString', ...
    ['Red lines: Rejected epochs', newline, ...
     'Green lines: Epochs selected for partial interpolation', newline, ...
     'Yellow lines: Channels/Components selected for interpolation/rejection', newline, ...
     'Black dots: Parts of channels selected for interpolation or highlighted during rejection']);
  y = y - h - tgap * 2;

  % < prev mark  |  > next mark
  halfW = (tw - gap) / 2;
  u(70) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin y halfW h], 'Style', 'pushbutton', 'FontSize', 8, ...
    'Tag', 'GoBackWinrej', 'BackgroundColor', DEFAULT_FIG_COLOR, 'string', '<', ...
    'Callback', {@draw_data,figh,11,[],[],ax1});

  u(71) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin+halfW+gap y halfW h], 'Style', 'pushbutton', 'FontSize', 8, ...
    'Tag', 'GoForwardWinrej', 'BackgroundColor', DEFAULT_FIG_COLOR, 'string', '>', ...
    'Callback', {@draw_data,figh,12,[],[],ax1});
