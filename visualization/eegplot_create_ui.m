  % Count trials
  u(29) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin y tw h], 'Style', 'text', 'FontSize', 8, ...
    'Tag', 'Count_Trials', 'BackgroundColor', DEFAULT_FIG_COLOR, 'string', '');
  y = y - h - tgap;

  % Count channels
  u(30) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin y tw h], 'Style', 'text', 'FontSize', 8, ...
    'Tag', 'Count_Channels', 'BackgroundColor', DEFAULT_FIG_COLOR, 'string', '');
  y = y - h - tgap * 2;

  % Rejection/Interpolation mode toggle
  displayep  = ['eegplot_adv(''redraw'');'];
  togglerej  = ['eegplot_adv(''rejection'');'];

  u(27) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin y tw h], ...
    'Tag', 'Rejection', 'BackgroundColor', DEFAULT_ON_COLOR, ...
    'string', 'Interpolation Mode', 'Callback', togglerej);
  y = y - h - tgap * 2;

  % EEG/ICA switch
  if g.EEG.plotchannels == 1
      cmodecolor = DEFAULT_ON_COLOR;
      cmode = 'Show ICA';
  else
      cmodecolor = DEFAULT_OFF_COLOR;
      cmode = 'Show EEG';
  end

  u(45) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin y tw h], ...
    'Tag', 'SWITCH', 'BackgroundColor', cmodecolor, ...
    'string', cmode, 'Callback', ['eegplot_adv(''SWITCH'');']);
  y = y - h - tgap * 2;

  % Epoch display toggle
  if g.trialstag == -1
      modecolor = DEFAULT_OFF_COLOR;
      mode = 'Show Epoch';
  else
      modecolor = DEFAULT_ON_COLOR;
      mode = 'Hide Epoch';
  end

  u(28) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin y tw h], ...
    'Tag', 'Display', 'BackgroundColor', modecolor, ...
    'string', mode, 'Callback', displayep);
  y = y - h - tgap * 2;

  % Normalize
  u(21) = uicontrol('Parent', panels.marksTab, 'Units', 'normalized', ...
    'Position', [margin y halfW h], ...
    'Tag', 'Norm', 'string', 'Norm', 'callback', {@normalize_chan,figh});

  % Stack/Spread
  cb_envelope = ['g = get(gcbf,''userdata'');' ...
      'hmenu = findobj(gcf, ''Tag'', ''Envelope_menu'');' ...
