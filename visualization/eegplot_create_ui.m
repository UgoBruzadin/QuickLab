  %% ========================================================================
  %  NAV PANEL - horizontal row of navigation controls
  %  ========================================================================
  bw  = 0.05;   % button width
  bwn = 0.04;   % narrow button width
  ew  = 0.12;   % edit width
  bh  = 0.8;    % button height (fraction of navPanel)
